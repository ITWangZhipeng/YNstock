"""
代码审查（Code Review）技能

功能描述:
    对 Python 项目进行全面代码审查，识别代码质量问题、潜在 bug、性能瓶颈和安全风险
    
适用场景:
    - Python Web 应用（FastAPI, Flask, Django 等）
    - 数据处理和分析脚本
    - WebSocket 实时通信应用
    - 批量数据处理任务
    - 测试代码质量检查

审查维度:
    1. 代码规范 (PEP 8)
        - 命名规范（变量、函数、类）
        - 代码格式和缩进
        - 导入语句组织
        - 行宽和空白行
        
    2. 代码质量
        - 重复代码检测
        - 函数复杂度分析
        - 代码可读性
        - 模块化程度
        
    3. 错误处理
        - 异常捕获完整性
        - 错误日志记录
        - 边界条件处理
        - 资源清理（文件、连接）
        
    4. 性能优化
        - 数据库查询优化
        - 循环效率
        - 内存使用
        - 异步/同步选择
        
    5. 安全性
        - 输入验证
        - SQL 注入防护
        - 敏感信息保护
        - 认证授权机制
        
    6. 可维护性
        - 注释和文档字符串
        - 类型注解
        - 单元测试覆盖
        - 配置管理

输出格式:
    🔴 严重问题 - 必须修复的 bug 或安全隐患
    🟡 警告级别 - 建议改进的设计问题
    🔵 优化建议 - 性能或可读性优化
    ✅ 优点亮点 - 值得保持的良好实践
    
使用示例:
    "使用代码审查技能检查这个 FastAPI 项目"
    "审查这段代码的质量问题"
    "检查这个 WebSocket 实现的潜在问题"

注意事项:
    - 优先关注严重问题（安全、bug）
    - 提供具体的修复建议
    - 指出问题的同时说明原因
    - 给出可操作的改进方案
"""


def code_review_checklist():
    """代码审查检查清单"""
    
    checklist = {
        'code_style': [
            '✓ 变量命名是否使用 snake_case',
            '✓ 类命名是否使用 PascalCase',
            '✓ 常量是否使用 UPPER_CASE',
            '✓ 函数是否有清晰的命名',
            '✓ 导入是否按标准库、第三方、本地模块分组',
            '✓ 是否避免使用魔术数字',
        ],
        
        'error_handling': [
            '✓ 是否捕获特定异常而非裸 except',
            '✓ 是否有适当的错误日志',
            '✓ 是否在 finally 中清理资源',
            '✓ 是否处理了边界情况（空值、None）',
            '✓ 是否有合理的重试机制',
        ],
        
        'performance': [
            '✓ 是否避免在循环中进行 I/O 操作',
            '✓ 是否使用生成器处理大数据',
            '✓ 是否避免不必要的数据库查询',
            '✓ 是否合理使用缓存',
            '✓ 是否使用异步处理 I/O 密集型任务',
        ],
        
        'security': [
            '✓ 用户输入是否经过验证',
            '✓ 密码等敏感信息是否加密存储',
            '✓ 是否避免硬编码密钥',
            '✓ 是否有速率限制防止滥用',
            '✓ SQL 查询是否使用参数化',
        ],
        
        'maintainability': [
            '✓ 函数是否单一职责',
            '✓ 函数长度是否合理（<50 行）',
            '✓ 是否有文档字符串',
            '✓ 是否有类型注解',
            '✓ 代码是否有适当注释',
            '✓ 配置文件是否与代码分离',
        ]
    }
    
    return checklist


def review_priority_guide():
    """审查优先级指南"""
    
    priorities = {
        'P0_紧急': [
            '安全漏洞（SQL 注入、XSS 等）',
            '数据泄露风险',
            '导致系统崩溃的 bug',
            '资源泄漏（连接、文件句柄）',
        ],
        'P1_高': [
            '逻辑错误',
            '未处理的异常',
            '并发问题',
            '性能瓶颈',
        ],
        'P2_中': [
            '代码重复',
            '命名不规范',
            '缺少文档',
            '代码复杂度过高',
        ],
        'P3_低': [
            '格式问题',
            '注释不完整',
            '可以优化的代码',
            '技术债务',
        ]
    }
    
    return priorities


